# Addition: Simple 5 aur 6 ko jod kar 11 dega
print(5+6)

# Subtraction: 15 mein se 6 ghata kar 9 dega
print(15-6)

# Multiplication: 15 aur 6 ko guna karke 90 dega
print(15*6)

# Division: Yeh hamesha float (decimal) mein answer dega, jaise 2.5
print(15/6)

# Floor Division: Yeh decimal hata deta hai aur sirf integer part deta hai (2.5 ka sirf 2)
print(15//6)

# Modulo Operator: Yeh division ke baad bacha hua 'Remainder' (sheshphal) batata hai
# 5 ko 3 se divide karo toh remainder 2 bachega
print(5%3)

# Exponential: Iska matlab hai 2 ki power 4 (2*2*2*2), answer aayega 16
print(2**4)



